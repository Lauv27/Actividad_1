# En la siguiente línea de Código se le pide al usuario ingresar un número.
numero = float(input("Ingrese un número: "))
# Con el número que se le pidió se hacen las siguientes operaciones.
cuadrado = numero ** 2 # En la siguiente ecuación se está elevando al cuadrado el número que el usuario ingreso de esta forma se calcula el cuadrado.
cubo = numero ** 3 # En la siguiente ecuación se está elevando al cubo el número que el usuario ingreso de esta forma se calcula el cubo.
# Mostrar los resultados.
print("El cuadrado de", numero, "es", cuadrado) # En esta línea de Código se muestra el resultado al cuadrado del número.
print("El cubo de", numero, "es", cubo) # En esta línea de Código se muestra el resultado al cubo del número.
